     		盆景起源<br/>
     		盆景源于中国。1972年在陕西乾陵发掘的唐代章怀太子墓(建于706年) 甬道东壁绘有侍女手托盆景的壁画，是迄今所知的世界上最早的盆景实录。宋代盆景已发展到较高的水平。当时的著名文士如王十朋、陆游、苏东坡等，都对盆景作过细致的描述和赞美。元代高僧韫上人制作小型盆景,取法自然，称“些子景”。明清时代盆景更加兴盛，已有许多关于盆景的著述问世。“盆景”一词，最早即见于明代屠隆所著的《考盘余事》 。20世纪50年代以后，盆景制作在公共园林、苗圃和民间家庭有了很大的普及，并成立了盆景协会，经常举办盆景园和盆景艺术展览等。日本的树桩盆景由中国传入，称“盆栽”。14世纪绘画中已有出现。1909年，日本盆栽通过伦敦的一次展览会而传到西方，第二次世界大战后始在欧美流行，并音译为“bonsai”。现美国、英国、联邦德国、澳大利亚和日本等都有全国性和地方性的协会。1980年在日本大阪召开了世界第一届盆景大会，同时举行了世界盆景展览。西安花卉盆景公司成立于2011年11月，面积1280亩，注册资金2000万元，员工39人，其中高级园艺师5人，中级技术人员15人。集园林种植、绿化设计、园林施工为一体的综合花卉种植企业。公司位于蓝田县城北环路中段路北，南临霸河、北临县委县政府。这里气候适宜、雨量充沛、鸟语花香,具有得天独厚的花卉盆景苗木栽培资源。公司按照标准化、规模化、集约化的方式建立了一个1千亩的名优花木生产种植基地，主要品种有：大规格朴树、国槐、皂角树、柿树、桂花、白皮松、北美海棠、法桐、青竹、紫薇、木瓜、红叶桃、榆叶梅、七叶树、大叶女贞、辛夷、红黄玉兰、垂柳、金丝柳 速生柳、等多个品种， 形成了规模化、市场化的布局。公司本着以诚信求生存，以科技求发展的经营理念，按照“区域化布局、规模化发展、标准化种植、娱乐休闲为一体”的格局，计划用3至5年时间，把公司打造成为鄢陵县花卉苗木、旅游、娱乐休闲为一体的名优产业基地，成为鄢陵县的一个靓点。鄢陵花卉种植历史悠久，在改革开放以来，国家明确提出了保护生态平衡、恢复植被、绿化环保、美化环境等一系列措施， 使我们有了更多的发展空间，公司期盼与社会各界人士携手共同打造一个生态环保、休闲旅游、花卉苗木一体化的生态园区。<br/><br/>
			盆景功效<br/>
			1、滴水观音<br/>
滴水观音——有清除空气灰尘的功效。（温馨提示：滴水观音茎内的白色汁液有毒，滴下的水也是有毒的，误碰或误食其汁液，就会引起咽部和口部的不适，胃里有灼痛感。应当特别注意防止幼儿误食。但是滴水观音并不属于致癌植物。）　
<br/>
2、非洲茉莉<br/>　

非洲茉莉---产生的挥发性油类具有显著的杀菌作用。可使人放松、有利于睡眠，还能提高工作效率。　
<br/>
3、白掌<br/>

白掌——抑制人体呼出的废气，如氨气和丙酮。同时它也可以过滤空气中的苯、三氯乙烯和甲醛。它的高蒸发速度可以防止鼻粘膜干燥，使患病的可能性大大降低。
<br/>
4、银皇后<br/>

银皇后——以它独特的空气净化能力著称：空气中污染物的浓度越高，它越能发挥其净化能力！因此它非常适合通风条件不佳的阴暗房间。　
<br/>
5、铁线蕨<br/>

铁线蕨——每小时能吸收大约20微克的甲醛，因此被认为是最有效的生物"净化器"。成天与油漆、涂料打交道者，或者身边有喜好吸烟的人，应该在工作场所放至少一盆蕨类植物。另外，它还可以抑制电脑显示器和打印机中释放的二甲苯和甲苯。
<br/>
6、鸭脚木<br/>

鸭脚木——给吸烟家庭带来新鲜的空气。叶片可以从烟雾弥漫的空气中吸收尼古丁和其他有害物质，并通过光合作用将之转换为无害的植物自有的物质。另外，它每小时能把甲醛浓度降低大约9毫克。　
<br/>
7、吊兰<br/>

吊兰——能吸收空气中95%的一氧化碳和85%的甲醛.

吊兰能在微弱的光线下进行光合作用,吊兰能吸收空气中的有毒有害气体，一盆吊兰在8~10平米的房间就相当于一个空气净化器。一般在房间内养1~2盆吊兰,能在24小时释放出氧气，同时吸收空气中的甲醛,苯乙烯，一氧化碳,二氧化碳等致癌物质.吊兰对某些有害物质的吸收力特别强，比如空气中混合的一氧化碳和甲醛分别能达到95%和85%.吊兰还能分解苯,吸收香烟烟雾中的尼古丁等比较稳定的有害物质。所以吊兰又被称为室内空气的绿色净化器.
<br/>
8、芦荟<br/>

芦荟——1盆芦荟相当于九台生物空气清洁器。盆栽芦荟有空气净化专家的美誉。一盆芦荟就等于九台生物空气清洁器，可吸收甲醛,二氧化碳，二氧化硫、一氧化碳等有害物质。尤其对甲醛吸收特别强.在4小时光照条件下。一盆芦荟可消除一平方米空气中90%的甲醛,还能杀灭空气中的有害微生物，并能吸附灰尘,对净化居室环境有很大作用。当室内有害空气过高时芦荟的叶片就会出现斑点.这就是求援信号。只要在室内再增加几盆芦荟.室内空气质量又会趋于正常。
