     		盆景浇水经验<br/>
			植物生长的过程是通过根系吸收水份和水份中的养料，再经干、枝输送到叶面蒸发掉，叶面毛细孔吸收一氧化碳和阳光中的紫外线经过循环制成所须养料供其生长发育，故盆景浇水十分重要。笔者在多年实践中总结经验点滴：

浇水看情况，掌握浇水量。

看盆的大、小、深、浅：大盆少浇，小盆多浇，深盆少浇，浅盆多浇

看盆的质地：土盆多浇，陶盆少浇，瓷盆再少浇

看土壤：粘土少浇，沙土多浇

看树种：阔叶树多浇，针叶树少浇，长势好的多浇，长势弱的少浇，树大盆小的多浇，树小盆大的少浇

看放置地点：向阳当晒的多浇，背阴少阳光的少浇；地势高风大的多浇，地势低风小的少浇，吹干风时多浇，空气湿度大时少浇。

浇水看时间，水质很关键。

盆景是否该浇水可用手指轻叩盆体，声响要浇，声音低沉不浇。且水温要与当地气温一致，过高会烫伤根，过低会冻伤根。在一般情况下，春秋季温差小，湿度大，不分时间干了才浇，夏季高温蒸发快，早晚浇，冬季植物休眠中午浇。忌用含盐碱和油腻的水。盆景多的用池塘、水池水，盆景少的可用换下的养鱼水或存放过的自来水。水从盆底流出为度，浇完后再喷洒叶面洗去灰尘以利光合作用。一般采用洒水壶浇，忌用自来水管直接浇盆景，以免造成盆土流失而危及植物生命。<br/><br/>
盆景无土栽培<br/>
盆景，尤其是树桩盆景，是有生命的雕塑艺术品。只有通过连年的养护管理才能达到叶色浓绿、花果满枝、姿优韵美、生机盎然，才能实现和维持它们理想的观赏装饰效果。如果养护管理跟不上，搞不好，再好的桩景也会因养护管理失误而造成枯萎死亡，其艺术生命也即终结。所以，养护管理是桩景创作和维持过程中的一项长期繁杂的工作。

传统的盆景植物通常栽植在盆土中，盆土是桩景植物生长的物质基础。

盆景无土栽培是盆景栽培管理中的一项新技术，是盆景历史上的一次革命。

盆景无土栽培的优点

盆景无土栽培即不用土壤而采用某些无机材料作基质浇灌营养液的栽培新技术。盆景无土栽培具有如下优点。

(1)生长快、周期短、质量高，盆景植物生长健壮、根系发达。无土栽培对盆景植物龙柏、雪松、紫杉、蜀桧、紫荆、雀梅、小叶榕、杜鹃、六月雪、火棘、小叶黄杨等有明显得促进生长作用，而且能增强盆景植物的抗寒耐暑和抵抗病虫害的能力，提高盆景的观赏效果。

(2)无毒、无臭、无菌，清洁卫生。无土栽培盆景不用土壤和有机肥．不污染周围环境，使其更适宜宾馆、饭店、办公室和居室的室内装饰，有利于盆景的普及，使盆景能进入千家万户。盆景无土栽培能彻底根绝有害病菌和虫卵，使植物处于无菌或少菌条件下进行栽培。无土栽培基质和营养液均无菌、无病虫隐患。

(3)为盆景出口创汇创造了有利条件。盆景是我国花卉生产和出口的拳头产品，在国际市场上占有绝对优势。但是，目前有些国家如美国、日本、荷兰、澳大利亚等都不允许带土入境，限制了我国盆景的出口。无土栽培盆景则完全解决了这些问题，为顺利通过海关检疫检查提供了技术保证。另外，无土盆景的重量大大减轻，便于搬运，为盆景批量出口提供了有利条件。

(4)可以充分发挥盆景的浅奇风格。盆景植物生长不靠土壤而用营养液，因此，基质可以减少到最低限度，盆钵可以更浅，从而更加突出景物的观赏效果。由于基质的微孔性，盆钵可以用更精美的材料制作，使盆景变得更加美观优雅。

(5)减轻劳动强度，节省劳力。无土栽培盆景简化了栽培中的翻盆换土、除草等工序，而且搬运轻便，大大节省了劳力。

(6)为盆景工厂化规模生产和电脑的应用开拓了新的前景。
